import prisma from './prisma.js';
import { runWithFirm } from './tenantContext.js';

// ---------------------------------------------------------------------------
// packRenderer — server-side HTML rendering for the two compliance packs
// the platform can email to recipients.
//
// Why this lives here, not duplicated in the worker
// -------------------------------------------------
// The Node API and the BullMQ email worker BOTH need to render a pack:
// the API for any future "preview" route, the worker for the actual
// attachment we ship to brokers / SRA inspectors. Rather than copy the
// HTML template into both places, we centralise it here.
//
// We deliberately reuse the same data shape the existing routes already
// produce — `routes/piiRenewal.ts` for PII renewal, and the SRA audit
// snapshot the FastAPI service produces for SRA inspection. This file
// only owns the HTML SHELL; data assembly lives where it always lived.
// That keeps "don't reinvent the PDF generator" honest.
// ---------------------------------------------------------------------------

export interface RenderedPack {
  html: string;
  filename: string;
}

export async function renderPackHtml(firmId: string, packType: string): Promise<RenderedPack> {
  // The worker enters this function inside an AsyncLocalStorage scope set
  // by the dispatch wrapper, so RLS context is already correct. The
  // explicit runWithFirm here is a belt-and-braces guard for any caller
  // that forgets — Prisma will throw without it.
  return runWithFirm(firmId, async () => {
    if (packType === 'pii_renewal') {
      const html = await renderPiiRenewalPack(firmId);
      const stamp = new Date().toISOString().split('T')[0];
      return { html, filename: `pii-renewal-pack-${stamp}.html` };
    }
    if (packType === 'sra_audit') {
      const html = await renderSraAuditPack(firmId);
      const stamp = new Date().toISOString().split('T')[0];
      return { html, filename: `sra-inspection-pack-${stamp}.html` };
    }
    throw new Error(`Unknown packType '${packType}'`);
  });
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------
function esc(s: unknown): string {
  if (s === null || s === undefined) return '—';
  return String(s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

function fmtDate(iso: Date | string | null | undefined): string {
  if (!iso) return '—';
  try {
    return new Date(iso).toLocaleDateString('en-GB');
  } catch {
    return '—';
  }
}

